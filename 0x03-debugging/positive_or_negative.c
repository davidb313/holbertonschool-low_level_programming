#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
 * positive_or_negative - this function assign a random number
 * executes and print the number
 * Return: 0 (Success)
 */
int main(void)
{
int i;

if (i > 0)
{
printf("%d is positive\n", i);
}
else if (i == 0)
{
printf("%d is zero\n", i);
}
else
{
printf("%d is negative\n", i);
}
return (0);
}
